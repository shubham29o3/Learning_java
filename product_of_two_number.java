import java.util.*;
    public class product_of_two_number {
        public static void main(String args[]) {
            Scanner sc = new Scanner(System.in);
            System.out.print("Enter a:- ");
            int a = sc.nextInt();
            System.out.print("Enter b: ");
            int b = sc.nextInt();
            int product = a * b;
            System.out.print("The product of a and b is: " + product);
        }
    }
